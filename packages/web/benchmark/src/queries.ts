import { buildFetchAnnotationsSQL } from "../../../core/services/DatabaseService";
import {
    buildGetFilesSQL,
    buildGetCountSQL,
} from "../../../core/services/FileService/DatabaseFileService";
import { buildDistinctValuesSQL } from "../../../core/services/AnnotationService/DatabaseAnnotationService";
import FileSet from "../../../core/entity/FileSet";
import FileFilter, { FilterType } from "../../../core/entity/FileFilter";
import FuzzyFilter from "../../../core/entity/FileFilter/FuzzyFilter";
import FileSort, { SortOrder } from "../../../core/entity/FileSort";
import { AnnotationType } from "../../../core/entity/AnnotationFormatter";

export interface BenchmarkQuery {
    name: string;
    /** Generate the SQL for a given table name */
    sql: (tableName: string) => string;
}

/**
 * Suite of queries generated by calling the exact same functions the app uses at runtime.
 *
 * Each entry delegates to an exported build*SQL function from the relevant service. If a dev
 * changes that function, the benchmark automatically runs the updated query — no manual sync needed.
 */
export const BENCHMARK_QUERIES: BenchmarkQuery[] = [
    // DatabaseService.fetchAnnotations()
    {
        name: "fetch_annotations",
        sql: (t) => buildFetchAnnotationsSQL(t),
    },
    // DatabaseFileService.getCountOfMatchingFiles()
    {
        name: "count_all",
        sql: (t) => buildGetCountSQL(t, new FileSet()),
    },
    // DatabaseFileService.getFiles() — sorted, paginated
    {
        name: "sort_and_paginate",
        sql: (t) =>
            buildGetFilesSQL(
                t,
                new FileSet({ sort: new FileSort("File Size", SortOrder.DESC) }),
                0,
                100
            ),
    },
    // DatabaseFileService.getFiles() — numeric FileFilter
    {
        name: "filter_by_size",
        sql: (t) =>
            buildGetFilesSQL(
                t,
                new FileSet({
                    filters: [
                        new FileFilter(
                            "File Size",
                            5000000,
                            FilterType.DEFAULT,
                            AnnotationType.NUMBER
                        ),
                    ],
                }),
                0,
                100
            ),
    },
    // DatabaseFileService.getFiles() — multiple FileFilters
    {
        name: "multi_column_filter",
        sql: (t) =>
            buildGetFilesSQL(
                t,
                new FileSet({
                    filters: [new FileFilter("cell_line", 3), new FileFilter("plate_id", 2)],
                }),
                0,
                100
            ),
    },
    // DatabaseAnnotationService.fetchFilteredValuesForAnnotation()
    {
        name: "distinct_values",
        sql: (t) => buildDistinctValuesSQL("cell_line", t),
    },
    // DatabaseFileService.getFiles() — FuzzyFilter text search
    {
        name: "text_search",
        sql: (t) =>
            buildGetFilesSQL(
                t,
                new FileSet({ filters: [new FuzzyFilter("File Name", "_5")] }),
                0,
                100
            ),
    },
];
